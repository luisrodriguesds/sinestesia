      <div class="modal-body" style="text-align: justify; padding: 20px;">
      	<p>
      		A palavra sinestesia vem de duas palavras gregas: sin (junto) e aisthesis (percepção). A sinestesia é uma condição neurológica, na qual um estímulo em uma modalidade sensorial desencadeia automaticamente uma sensação secundária em outra ou na mesma modalidade sensorial. Para um sinesteta, indivíduo que tem sinestesia, as letras, os números, os dias da semana ou os meses do ano podem ser associados a cores, sons, sabores, odores ou sensações táteis específicas. Por exemplo, o tipo mais comum de sinestesia, chamada de sinestesia grafema-cor, está relacionado à associação entre grafemas (letras e números) e cores. Para um sinesteta grafema-cor, a letra M pode evocar automaticamente a cor laranja claro e o número 3 a cor verde jade.
      	</p>
      	<p>
			A experiência sinestésica é aditiva, ou seja, ela nunca substitui a percepção primária. Se, para um sinesteta música-cor, a nota musical LÁ está associada à cor azul, o som da nota não será de nenhum modo comprometido, porém, diferentemente de outras pessoas, paralelamente à experiência do som acontece a experiência da cor.
      	</p>
